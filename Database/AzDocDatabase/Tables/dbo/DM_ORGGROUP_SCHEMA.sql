	[SchemaId] int NOT NULL,
	[OrganizationId] int NOT NULL,
	[GroupTypeId] int NOT NULL,
    CONSTRAINT [PK_DM_ORGGROUP_SCHEMA] PRIMARY KEY ([OrgGroupId]),
