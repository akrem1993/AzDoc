    CONSTRAINT [FK_DM_ORGGROUP_SCHEMA_DM_ORGGROUP_TYPE] FOREIGN KEY([GroupTypeId]) REFERENCES [dbo].[DM_ORGGROUP_TYPE] ([GroupTypeId]),
    CONSTRAINT [FK_DM_ORGGROUP_SCHEMA_DM_SCHEMA] FOREIGN KEY([SchemaId]) REFERENCES [dbo].[DM_SCHEMA] ([SchemaId])
 )
